SyntaxCheckRightBracket:
		lda 	(codePtr),y
		iny
		cmp 	#TOK_RPAREN
		bne 	_SCRBError
		rts
_SCRBError:
		berror	"Missing )"		
		
; *****************************************************************************
;
;									Check General
;
; *****************************************************************************

SyntaxCheckA:
		cmp 	(codePtr),y
		bne 	_SCAError
		iny
		rts
_SCAError:
		jmp 	SyntaxError
				